    private void btnGuardarCategoriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCategoriaActionPerformed
        if (!campoCatalogo1.getText().equals("")){
            catalogoCategoria.add(new Categoria(campoCatalogo1.getText().toUpperCase(), ++contadorCategorias));
            
            //Se guarda el objeto Categoría temporalmente
            Categoria c = catalogoCategoria.get(contadorCategorias -1);
            //Se agrega la Categoría al modelo del comboBox
            modeloComboBoxCatalogo.addElement(c);
            
            //Se agrega la Categoría al modelo de la tabla Categorias
            String [] datos = {String.valueOf(c.getIdCategoria()), c.getNombreCategoria()};
            modeloTablaCategorias.addRow(datos);
            
            
            campoCatalogo1.setText("");
            JOptionPane.showMessageDialog(
                rootPane, "Categoría guardada exitosamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        }else
        JOptionPane.showMessageDialog(
            rootPane, "Ingrese el nombre de la categoria", "Advertencia", JOptionPane.WARNING_MESSAGE);

    }//GEN-LAST:event_btnGuardarCategoriaActionPerformed

