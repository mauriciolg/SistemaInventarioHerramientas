    private void btnGuardarHerramientaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarHerramientaActionPerformed
        if(modeloComboBoxCatalogo.getSize() < 1){
            JOptionPane.showMessageDialog(
                rootPane, "¡Ups, No hay Categorias!\nIngrese Nueva Categoria", "Advertencia", JOptionPane.WARNING_MESSAGE);
        } else
        if(!"".equals(campoCatalogo2.getText())){
            catalogoHerramienta.add(new Herramienta(
                campoCatalogo2.getText().toUpperCase(),(Categoria) comboBoxCatalogo.getSelectedItem(), ++contadorHerramientas));
            
            //Se guarda temporalmente la herramienta
            Herramienta h = catalogoHerramienta.get(contadorHerramientas-1);
            //Se guarda la Herramienta en el modelo de la Tabla Herramientas
            String[] datos = {String.valueOf(h.getIdHerramienta()), h.getCategoriaHerramienta().toString(), h.getNombreHerramienta()};
            modeloTablaHerramientas.addRow(datos);

        JOptionPane.showMessageDialog(
            rootPane, "Herramienta guardada exitosamente", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
        campoCatalogo2.setText("");
        comboBoxCatalogo.setSelectedIndex(0);
        }else JOptionPane.showMessageDialog(
            rootPane, "Añada el nombre de la herramienta", "Advertencia", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_btnGuardarHerramientaActionPerformed

